package ch5;

import java.util.Arrays;

public class howToPrintArray {

	public static void main(String[] args) {

		String[] names = null;
		names = new String[] { "aa", "vd" };
		System.out.println(Arrays.toString(names));

		int[] num;
		num = new int[] { 1, 2 };
		System.out.println(Arrays.toString(num));

	}

}
